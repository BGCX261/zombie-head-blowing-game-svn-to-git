----------------------------------------------------------------------------------------------------------------------------

-- state start

----------------------------------------------------------------------------------------------------------------------------
function setting_start(me)

	--	루트 패널을 가져온다.
	root = me:GetRootPanel();
	root:AppendUI( me:MakeImage(200,0, "title", 0, true) );

	--  아이콘 부착
	icon = root:AppendUI( me:MakeDoAppeal(-500, 500, 0, 0) );
	icon = icon:AppendUI( me:MakeDoClick(100) );
	icon:AppendUI( me:MakeImage(0,250, "titlestart", 0, true) );
end


----------------------------------------------------------------------------------------------------------------------------

-- state init

----------------------------------------------------------------------------------------------------------------------------
function setting_init(me)

end


----------------------------------------------------------------------------------------------------------------------------

-- state event

----------------------------------------------------------------------------------------------------------------------------
function setting_event(me, evttype, evtidx)
	if UE_TOUCH_UP == evttype then
		if evtidx == 100 then
			-- me:SetRootPanel( UI_MAINTITLE );
			me:SendEvent(USER_INGAME,0);
		end
	end
end






