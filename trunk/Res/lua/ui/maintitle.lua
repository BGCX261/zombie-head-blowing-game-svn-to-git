----------------------------------------------------------------------------------------------------------------------------

-- state start

----------------------------------------------------------------------------------------------------------------------------
function maintitle_start(me)

	--	루트 패널을 가져온다.
	root = me:GetRootPanel();
	root:AppendUI( me:MakeImage(0,0, "title", 0, true) );

	--  아이콘 부착
	icon = root:AppendUI( me:MakeDoAppeal(0, -400, 0, 0) );
	icon = icon:AppendUI( me:MakeDoClick(100) );
	icon:AppendUI( me:MakeImage(0,-250, "titlestart", 0, true) );
end


----------------------------------------------------------------------------------------------------------------------------

-- state init

----------------------------------------------------------------------------------------------------------------------------
function maintitle_init(me)

end


----------------------------------------------------------------------------------------------------------------------------

-- state event

----------------------------------------------------------------------------------------------------------------------------
function maintitle_event(me, evttype, evtidx)
	if UE_TOUCH_UP == evttype then
		if 100 == evtidx then
			me:SetRootPanel( UI_SETTING );
		end	
	end
end






